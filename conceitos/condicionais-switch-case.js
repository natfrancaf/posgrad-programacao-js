/**
 * Condicionais
 *   switch case
 *  utilizado quando tem mais de 3 condicionais
 */

const peso = 31

switch(peso){
    case 10:
        console.log('P')
        break
    case 20:
        console.log('M')
        break
    case 30:
        console.log('G')
        break
    default:
        console.log('ND')
}

