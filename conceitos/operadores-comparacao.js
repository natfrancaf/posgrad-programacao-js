/*
    > maior que
    >=maior ou igual que
    < menor que
    <= menor ou igual que
    == igualdade somente de valores
    != diferença somente de valores
    === igualdade de valores e tipo de dado


*/
//maior que
console.log(10>5) //verdadeiro
console.log(10>20) //falso

//maior ou igual
console.log(10 >= 10)

//menor que
console.log(10 < 5) //falso

//menor ou igual

console.log(10 <= 10)

//igualdade somente valores

console.log(1 == '1') // sao tipos diferentes mas considera verdadeiro


//igualdade de tipo e valores - RECOMENDADO USAR ESSE

console.log(1 === '1') // falso pq os tipos são diferentes

//diferença de valores

console.log(1 != 2)
console.log(1 !== 2) //considera tipagem